def validate(classes, objprops, dataprops):
    laid_classes = set(NODE_CLASS)
    missing_c = set(classes) - laid_classes
    if missing_c:
        sys.exit(f"ERROR: ontology classes not placed in NODES layout: {sorted(missing_c)}\n"
                 f"       Add a box for each, then re-run.")
    laid_props = {e["prop"] for e in EDGES if e.get("prop")}
    # A subproperty is covered when its parent is drawn (the parent arrow stands for it).
    covered = set(laid_props)
    for p, info in objprops.items():
        if info.get("subPropertyOf") and info["subPropertyOf"][0] in laid_props:
            covered.add(p)
    missing_p = set(objprops) - covered
    if missing_p:
        sys.exit(f"ERROR: ontology object properties not drawn as EDGES: {sorted(missing_p)}\n"
                 f"       Add an arrow for each (or make it a subproperty of a drawn one), then re-run.")
    # Every datatype property must land in a card: its domain's card, or (no
    # domain — plan-wide by convention, e.g. taktDuration) the TaktGraph card.
    for p, i in dataprops.items():
        home = i["domain"] or "TaktGraph"
        if home not in NODES:
            sys.exit(f"ERROR: datatype property '{p}' has domain '{i['domain']}' with no card in NODES.")
    phantom_c = laid_classes - set(classes)
    phantom_p = laid_props - set(objprops)
    if phantom_c or phantom_p:
        print(f"WARNING: layout references terms absent from the ontology: "
              f"classes={sorted(phantom_c)} props={sorted(phantom_p)}")


