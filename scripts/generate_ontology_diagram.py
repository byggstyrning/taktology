def font_path(bold: bool) -> str:
    name = "DejaVuSans-Bold.ttf" if bold else "DejaVuSans.ttf"
    p = FONT_DIR / name
    if p.exists():
        return str(p)
    try:  # fallback: borrow matplotlib's bundled copy
        import matplotlib
        mp = Path(matplotlib.__file__).parent / "mpl-data" / "fonts" / "ttf" / name
        if mp.exists():
            return str(mp)
    except Exception:
        pass
    sys.exit(f"font {name} not found in {FONT_DIR}")


def fnt(bold: bool, size: int):
    return ImageFont.truetype(font_path(bold), int(sc(size)))


def line(d, p, q, color, width):
    d.line([sc(p[0]), sc(p[1]), sc(q[0]), sc(q[1])], fill=color, width=int(sc(width)))


def polyline(d, pts, color, width):
    d.line([sc(c) for p in pts for c in p], fill=color, width=int(sc(width)), joint="curve")


def border_point(box, toward):
    x0, y0, x1, y1 = box
    cx, cy = (x0 + x1) / 2, (y0 + y1) / 2
    dx, dy = toward[0] - cx, toward[1] - cy
    if dx == 0 and dy == 0:
        return cx, cy
    sx = (x1 - cx) / dx if dx > 0 else ((x0 - cx) / dx if dx < 0 else math.inf)
    sy = (y1 - cy) / dy if dy > 0 else ((y0 - cy) / dy if dy < 0 else math.inf)
    s = min(sx, sy)
    return cx + dx * s, cy + dy * s


def arrowhead(d, tip, frm, color, size=13):
    ang = math.atan2(tip[1] - frm[1], tip[0] - frm[0])
    a1, a2 = ang + math.radians(152), ang - math.radians(152)
    p1 = (tip[0] + size * math.cos(a1), tip[1] + size * math.sin(a1))
    p2 = (tip[0] + size * math.cos(a2), tip[1] + size * math.sin(a2))
    d.polygon([sc(tip[0]), sc(tip[1]), sc(p1[0]), sc(p1[1]), sc(p2[0]), sc(p2[1])], fill=color)


def cubic(p0, p1, p2, p3, n=60):
    out = []
    for i in range(n + 1):
        t = i / n
        u = 1 - t
        x = u**3 * p0[0] + 3 * u**2 * t * p1[0] + 3 * u * t**2 * p2[0] + t**3 * p3[0]
        y = u**3 * p0[1] + 3 * u**2 * t * p1[1] + 3 * u * t**2 * p2[1] + t**3 * p3[1]
        out.append((x, y))
    return out


def quad(p0, p1, p2, n=60):
    out = []
    for i in range(n + 1):
        t = i / n
        u = 1 - t
        x = u**2 * p0[0] + 2 * u * t * p1[0] + t**2 * p2[0]
        y = u**2 * p0[1] + 2 * u * t * p1[1] + t**2 * p2[1]
        out.append((x, y))
    return out


def dashed_polyline(d, pts, color, width, dash=12, gap=8):
    draw_on, acc = True, 0.0
    for a, b in zip(pts, pts[1:]):
        seg = math.hypot(b[0] - a[0], b[1] - a[1])
        if seg == 0:
            continue
        ux, uy = (b[0] - a[0]) / seg, (b[1] - a[1]) / seg
        pos = 0.0
        while pos < seg:
            step = min((dash if draw_on else gap) - acc, seg - pos)
            x0, y0 = a[0] + ux * pos, a[1] + uy * pos
            x1, y1 = a[0] + ux * (pos + step), a[1] + uy * (pos + step)
            if draw_on:
                line(d, (x0, y0), (x1, y1), color, width)
            pos += step
            acc += step
            if acc >= (dash if draw_on else gap):
                draw_on = not draw_on
                acc = 0.0


def text_center(d, xy, text, font, color, halo=False, pad=(6, 2.5), radius=8):
    cx, cy = sc(xy[0]), sc(xy[1])
    if halo:
        bb = d.textbbox((cx, cy), text, font=font, anchor="mm")
        px, py = int(sc(pad[0])), int(sc(pad[1]))
        d.rounded_rectangle([bb[0] - px, bb[1] - py, bb[2] + px, bb[3] + py],
                            radius=int(sc(radius)), fill=COL["pill"], outline=COL["pill_edge"],
                            width=int(sc(1)))
    d.text((cx, cy), text, font=font, fill=color, anchor="mm")


def text_left(d, xy, text, font, color):
    d.text((sc(xy[0]), sc(xy[1])), text, font=font, fill=color, anchor="lm")


def badge(d, xy, n, f, r=11):
    cx, cy = xy
    d.ellipse([sc(cx - r), sc(cy - r), sc(cx + r), sc(cy + r)],
              fill=COL["badge"], outline=(255, 255, 255), width=int(sc(2)))
    d.text((sc(cx), sc(cy + 0.5)), str(n), font=f, fill=(255, 255, 255), anchor="mm")


def rounded(d, box, radius, fill, outline, width=1.6):
    d.rounded_rectangle([sc(box[0]), sc(box[1]), sc(box[2]), sc(box[3])],
                        radius=sc(radius), fill=fill, outline=outline, width=int(sc(width)))


def hatch(d, box, color, step=9, width=1.6):
    x0, y0, x1, y1 = box
    n = int(((x1 - x0) + (y1 - y0)) / step) + 1
    for i in range(n):
        ax = x0 + i * step
        sxp, syp = min(ax, x1), y0 + max(0, ax - x1)
        exp, eyp = x0 + max(0, i * step - (y1 - y0)), min(y0 + i * step, y1)
        if syp <= y1 and exp <= x1:
            line(d, (sxp, syp), (exp, eyp), color, width)


def pill_rows(d, cx, y, pills, f, max_w, gap=8, row_h=27):
    """Lay pills into centred rows within max_w. Returns the next free y."""
    pad = 9
    sizes = [(d.textlength(t, font=f) / SS + 2 * pad, t, v) for t, v in pills]
    rows, cur, cur_w = [], [], 0.0
    for w_, t, v in sizes:
        if cur and cur_w + gap + w_ > max_w:
            rows.append(cur)
            cur, cur_w = [], 0.0
        cur.append((w_, t, v))
        cur_w += (gap if len(cur) > 1 else 0) + w_
    if cur:
        rows.append(cur)
    for row in rows:
        total = sum(w_ for w_, *_ in row) + gap * (len(row) - 1)
        x = cx - total / 2
        for w_, t, v in row:
            c = VOCAB[v]
            rounded(d, (x, y - 11, x + w_, y + 11), 11, c["fill"], c["edge"], 1.4)
            text_center(d, (x + w_ / 2, y), t, f, c["text"])
            x += w_ + gap
        y += row_h
    return y


