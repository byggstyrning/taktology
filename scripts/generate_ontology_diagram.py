def class_pills(name, info):
    """[(glyph+text, vocab)] pills for a takt class card, read from the ttl."""
    pills = []
    note = NODES[name].get("note")
    if note:
        pills.append((note, "note"))
    for q in info["subClassOfQ"]:
        pills.append(("⊑ " + q, q.split(":")[0]))
    for q in info["closeMatchQ"]:
        pills.append(("≈ " + q, q.split(":")[0]))
    for q in info["relatedMatchQ"]:
        pills.append(("~ " + q, q.split(":")[0]))
    return pills


def card_dataprops(name, dataprops):
    """Datatype chips shown inside a class card (domain-less -> TaktGraph)."""
    out = []
    for p, i in dataprops.items():
        home = i["domain"] or "TaktGraph"
        if home == name:
            out.append(p + (" (plan-wide)" if i["domain"] is None else ""))
    return sorted(out)


